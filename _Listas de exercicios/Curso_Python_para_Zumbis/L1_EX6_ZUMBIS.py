print('L1_E6_Zumbis \n====Calcule o tempo de uma viagem de carro. Pergunte a distância a percorrer e a velocidade média esperada para a viagem=====')
v = int(input('\nOlá, digite a velocidade média (km/h): '))
d = int(input('Agora digite a distância a percorrer (km): '))
t = d/v
print('\n O tempo estimado de viagem é {} h' .format(t))
print('\n Ou {} minutos' .format(t*60))
